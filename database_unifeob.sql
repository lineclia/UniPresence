-- Calcular Faltas e Presenças por Disciplina usando parte do nome da disciplina
DELIMITER //

CREATE PROCEDURE calcular_faltas_presencas_disciplina_nome_parcial(
    IN ra_aluno VARCHAR(20),
    IN nome_parcial_disciplina VARCHAR(100)
)
BEGIN
    SELECT 
        a.nome AS Aluno,
        d.nome_disciplina AS Disciplina,
        COUNT(CASE WHEN rp.status_presenca = 'Presente' THEN 1 END) AS "Total Presencas",
        COUNT(CASE WHEN rp.status_presenca = 'Ausente' THEN 1 END) AS "Total Faltas"
    FROM 
        registro_presenca rp
    JOIN 
        aula au ON rp.idaula = au.idaula
    JOIN 
        grade_horaria g ON au.id_grade = g.id_grade
    JOIN 
        disciplina d ON g.iddisciplina = d.iddisciplina
    JOIN 
        aluno a ON rp.idaluno = a.idaluno
    WHERE 
        a.ra = ra_aluno
        AND d.nome_disciplina LIKE CONCAT('%', nome_parcial_disciplina, '%')
    GROUP BY 
        a.nome, d.nome_disciplina;
END //

DELIMITER ;

-- Calcular Faltas e Presenças por Módulo

DELIMITER //

CREATE PROCEDURE calcular_faltas_presencas_modulo_ra(
    IN ra_aluno VARCHAR(20),
    IN modulo INT
)
BEGIN
    SELECT 
        a.nome AS NomeAluno,
        c.nomeCurso AS Curso,
        a.modulo AS Modulo,
        COUNT(CASE WHEN rp.status_presenca = 'Presente' THEN 1 END) AS TotalPresencas,
        COUNT(CASE WHEN rp.status_presenca = 'Ausente' THEN 1 END) AS TotalFaltas
    FROM 
        registro_presenca rp
    JOIN 
        aula au ON rp.idaula = au.idaula
    JOIN 
        grade_horaria g ON au.id_grade = g.id_grade
    JOIN 
        disciplina d ON g.iddisciplina = d.iddisciplina
    JOIN 
        aluno a ON rp.idaluno = a.idaluno
    JOIN 
        curso c ON a.idcurso = c.idcurso
    WHERE 
        a.ra = ra_aluno
        AND a.modulo = modulo
    GROUP BY 
        a.nome, c.nomeCurso, a.modulo;
END //

DELIMITER ;

